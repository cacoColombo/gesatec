    private void botaoSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoSalvarActionPerformed
        try {
            if ( this.validarCampos() )
            {
                TipoDeAtendimento tipoDeAtendimento = new TipoDeAtendimento();
                if ( id.getText().length() > 0 )
                {
                    tipoDeAtendimento.setId(Integer.parseInt(id.getText()));
                }
                tipoDeAtendimento.setNome(nome.getText());
                tipoDeAtendimento.setAtivo(ativo.isSelected());
                
                if ( id.getText().length() > 0 ) {
                    DAO.getInstance().merge(tipoDeAtendimento);
                } else {
                    DAO.getInstance().persist(tipoDeAtendimento);
                }

                parent.atualizarGrid(tipoDeAtendimento.getId(), new ArrayList());
                JOptionPane.showMessageDialog(this, "Registro efetuado com sucesso!", "Sucesso!", JOptionPane.INFORMATION_MESSAGE);
                
                this.setVisible(false);
            }
        } catch (Exception err) {
            SOptionPane.showMessageDialog(this, err, "Erro!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_botaoSalvarActionPerformed

